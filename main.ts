  /**
   * Play selected sound
   */
  //% block
  export function play(soundId: SoundIds) {
    const sound = soundData[soundId]
      .split("")
      .map((l: string) =>
        l.charCodeAt(0) >= 65 ? repeatZero(l.charCodeAt(0) - 64) : l
      )
      .join("");
    music.__playSoundExpression(sound, true);
  }
